package com.infinite.core;

public class leapyear {
	  public static void main(String[] args) {

		    // year to be checked
		    int year = 2023;

		    // if the year is divided by 4
		    if (year % 4 == 0) {
		      System.out.println(year + " is a leap year.");
		    }
		    else{
		      System.out.println(year + " is not a leap year.");
		  }


}
}
